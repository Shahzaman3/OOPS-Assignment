#include <iostream>
using namespace std;

int main() {
    cout << "Data Types and Their Sizes (in bytes)\n";

    cout << "int: " << sizeof(int) << " bytes\n";
    cout << "float: " << sizeof(float) << " bytes\n";
    cout << "double: " << sizeof(double) << " bytes\n";
    cout << "char: " << sizeof(char) << " byte\n";
    cout << "short: " << sizeof(short) << " bytes\n";
    cout << "long: " << sizeof(long) << " bytes\n";
    cout << "boolean: " << sizeof(bool) << " byte\n";

    return 0;
}
